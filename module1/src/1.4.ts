//basic data type

//string
let firstName: string = "adnan";
//number
let roll: number = 123;
//boolean
let isAdmin: boolean = true;
//undefined
let x: undefined = undefined;
//null
let y: null = null;
//any
let d: any;
d = 123;

//none primitive data type
//array string
let friends: string[] = ["ali", "ahmed", "umer"];

//array number
let marks: number[] = [12, 23, 34];

friends.push("ontor");

//tuple type --> array with fixed number of elements
let info: object = {
  name: "adnan",
  age: 23,
};

//tuple type --> array with fixed number of elements
let coordinates: [number, number] = [12, 23];
let age: [string, number, boolean] = ["adnan", 23, true];
